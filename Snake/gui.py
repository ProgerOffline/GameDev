	def createImageLevel(self):
		"""Создает картинку уровня на основе списка self.level"""
		canvas = pygame.Surface((441, 441), pygame.SRCALPHA, 32)

		x = 1
		y = 1

		for i in self.level:
			if i == 0:
				pygame.draw.rect(canvas, yellow, pygame.Rect(x, y, 10, 10))

			x += 11
			if x == 441:
				y += 11
				x = 1

		pygame.image.save(canvas, "img/theFirst.png")

	def drawLevel(self, screen):
		"""Рисует уровень на заднем фоне"""
		screen.blit(self.levelImage, (0, 0))

	def initField(self):
		"""Заполняет списки с кординатам обьектов"""

		x = 1
		y = 1

		for i in self.level:
			if i == 0:
				self.barrier.append([x, y])
			elif i == 1 and y != 12:
				self.field.append([x, y])

			x += 11

			if x == 441:
				y += 11
				x = 1

	def getNewIndicator(self):
		"""Заполняет список счета/координаты"""
		self.indicator.append([self.indicator[-1][0] + 11, 12])

	def drawIndicator(self, screen):
		"""Отрисовка индикотора"""
		for i in self.indicator:
			pygame.draw.rect(screen, green, pygame.Rect(i[0], i[1], 10, 10))

	def drawWin(self, screen):
		"""Отображаем каритнку победителя"""
		screen.blit(self.winImage, (23, 100))	

	def drawLose(self, screen):
		"""Отображаем каритнку проигрыша"""
		screen.blit(self.loseImage, (23, 100))

	def drawPauseImage(self, screen):
		"""Отображаем картинку паузы"""
		screen.blit(self.pauseImage, (90, 90))

	def checkWinOrLose(self):
		"""Отслеживаем проиграли игрок или выиграл"""
		if len(self.indicator) == 0:
			self.onGame = "LOSE"
		elif len(self.indicator) == 37:
			self.onGame = "WIN"